<script setup lang="ts">
import { reactive, ref, onMounted, computed } from 'vue'
import { useAuth } from '~/composables/useAuth'
import { useRouter } from 'vue-router'
import { useToast } from '~/composables/useToast'
import { $fetch } from 'ofetch'

interface UserForm {
  fullName: string;
  email: string;
  password: string;
  firstName: string;
  lastName: string;
  phone: string;
  address: string;
  city: string;
  country: string;
  pincode: number;
}

const router = useRouter()
const { loadFromStorage, isLoggedIn, clearAuth } = useAuth()
const saving = ref(false)
const message = ref('')
const successFlag = ref(false)
const { success, error } = useToast()
const showLogoutConfirm = ref(false)

const userData = reactive<UserForm>({
  fullName: "",
  email: "",
  password: "",
  firstName: "",
  lastName: "",
  phone: "",
  address: "",
  city: "",
  country: "",
  pincode: 0
})

onMounted(async () => {
  loadFromStorage()
  if (!isLoggedIn.value) {
    router.push('/LoginPage')
    return
  }
  await loadUserData()
})

async function loadUserData() {
  try {
    const token = localStorage.getItem('jwt_token') || sessionStorage.getItem('jwt_token')
    if (!token) return
    
    const me = await $fetch<any>('/api/auth/me', { 
      headers: { Authorization: `Bearer ${token}` } 
    })
    
    if (me) {
      userData.fullName = me.name || ''
      userData.email = me.email || ''
      userData.phone = me.phone || ''
      userData.address = me.address || ''
      userData.city = me.city || ''
      userData.country = me.country || ''
      userData.pincode = me.pincode ? Number(me.pincode) : 0
      
      if (userData.fullName) {
        const parts = userData.fullName.split(' ')
        userData.firstName = parts.shift() || ''
        userData.lastName = parts.join(' ')
      }
    }
  } catch (e: any) {
    console.error('Failed to load profile', e)
    message.value = e?.statusMessage || 'Failed to load profile'
  }
}

async function handlesubmit () {
  message.value = ''
  successFlag.value = false
  if (!userData.firstName.trim()) {
    message.value = 'Please provide at least your first name.'
    error('Please provide at least your first name.', 'Validation Error')
    return
  }
  saving.value = true
  try {
    const token = localStorage.getItem('jwt_token') || sessionStorage.getItem('jwt_token')
    if (!token) {
      message.value = 'Not authenticated'
      error('Please login first', 'Authentication Required')
      saving.value = false
      return
    }
    
    const payload = {
      firstName: userData.firstName || null,
      lastName: userData.lastName || null,
      phone: userData.phone || null,
      address: userData.address || null,
      city: userData.city || null,
      country: userData.country || null,
      pincode: userData.pincode ? String(userData.pincode) : null,
    }
    
    const res: any = await $fetch('/api/users/me', {
      method: 'PUT',
      body: payload,
      headers: { Authorization: `Bearer ${token}` }
    })
    
    if (res) {
      userData.fullName = res.name || ''
      userData.email = res.email || ''
      userData.phone = res.phone || ''
      userData.address = res.address || ''
      userData.city = res.city || ''
      userData.country = res.country || ''
      userData.pincode = res.pincode ? Number(res.pincode) : 0
      
      if (userData.fullName) {
        const parts = userData.fullName.split(' ')
        userData.firstName = parts.shift() || ''
        userData.lastName = parts.join(' ')
      }
    }
    
    successFlag.value = true
    message.value = 'Profile saved to database successfully!'
    success('Profile saved successfully!', 'Profile Updated')
  } catch (e: any) {
    console.error('Save error:', e)
    message.value = e?.statusMessage || e?.data?.message || 'Failed to save profile.'
    error(message.value, 'Save Failed')
  } finally {
    saving.value = false
  }
}

function handleLogout() {
  showLogoutConfirm.value = true
}

function confirmLogout() {
  clearAuth()
  localStorage.removeItem('profile_data')
  success('Logged out successfully', 'Goodbye!')
  router.push('/')
}

function cancelLogout() {
  showLogoutConfirm.value = false
}
</script>
<template>
  <div class="bg-gray-50 min-h-screen py-8">
    <div class="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8">
      
      <div class="bg-white rounded-2xl shadow-lg p-6 md:p-8 mb-8">
        <div class="flex flex-col md:flex-row items-center md:items-start gap-6">
          
          <div class="flex-1 text-center md:text-left">
            <h1 class="text-3xl font-bold text-gray-900">{{ userData.fullName || 'Welcome' }}</h1>
            <p class="text-gray-600 mt-1">{{ userData.email }}</p>
            <div class="flex flex-wrap gap-2 mt-4 justify-center md:justify-start">
              <span class="px-3 py-1 bg-blue-100 text-blue-700 rounded-full text-sm font-semibold">Member</span>
              <span v-if="userData.city" class="px-3 py-1 bg-gray-100 text-gray-700 rounded-full text-sm">{{ userData.city }}</span>
            </div>
          </div>

          <div class="flex flex-col gap-2">
            <button @click="handleLogout" class="px-6 py-2 border-2 border-red-500 text-red-600 rounded-lg font-semibold hover:bg-red-50 transition-all">
              Logout
            </button>
          </div>
        </div>
      </div>

      <div class="bg-white rounded-2xl shadow-lg">
        <div class="p-6 md:p-8">
            <form @submit.prevent="handlesubmit">
              <div v-if="message" :class="['mb-6 px-4 py-3 rounded-lg', successFlag ? 'bg-green-50 text-green-700 border border-green-200' : 'bg-red-50 text-red-700 border border-red-200']">
                {{ message }}
              </div>

              <div class="mb-8">
                <h3 class="text-xl font-semibold text-gray-900 mb-4">Personal Information</h3>
                <div class="grid grid-cols-1 md:grid-cols-2 gap-6">
                  <div>
                    <label class="block text-sm font-medium text-gray-700 mb-2">First Name *</label>
                    <input
                      v-model="userData.firstName"
                      type="text" 
                      required
                      placeholder="Enter first name" 
                      class="w-full px-4 py-3 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500 transition-all"
                    />
                  </div>
                  <div>
                    <label class="block text-sm font-medium text-gray-700 mb-2">Last Name</label>
                    <input 
                      v-model="userData.lastName" 
                      type="text" 
                      placeholder="Enter last name" 
                      class="w-full px-4 py-3 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500 transition-all"
                    />
                  </div>
                  <div>
                    <label class="block text-sm font-medium text-gray-700 mb-2">Email</label>
                    <input 
                      v-model="userData.email" 
                      type="email" 
                      disabled
                      class="w-full px-4 py-3 border border-gray-300 rounded-lg bg-gray-50 cursor-not-allowed"
                    />
                    <p class="text-xs text-gray-500 mt-1">Email cannot be changed</p>
                  </div>
                  <div>
                    <label class="block text-sm font-medium text-gray-700 mb-2">Phone Number</label>
                    <input 
                      v-model="userData.phone" 
                      type="tel" 
                      placeholder="Enter phone number" 
                      class="w-full px-4 py-3 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500 transition-all"
                    />
                  </div>
                </div>
              </div>

              <div class="mb-8">
                <h3 class="text-xl font-semibold text-gray-900 mb-4">Address Information</h3>
                <div class="space-y-4">
                  <div>
                    <label class="block text-sm font-medium text-gray-700 mb-2">Street Address</label>
                    <input 
                      v-model="userData.address" 
                      type="text" 
                      placeholder="Enter street address" 
                      class="w-full px-4 py-3 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500 transition-all"
                    />
                  </div>
                  <div class="grid grid-cols-1 md:grid-cols-3 gap-4">
                    <div>
                      <label class="block text-sm font-medium text-gray-700 mb-2">City</label>
                      <input 
                        v-model="userData.city" 
                        type="text" 
                        placeholder="Enter city" 
                        class="w-full px-4 py-3 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500 transition-all"
                      />
                    </div>
                    <div>
                      <label class="block text-sm font-medium text-gray-700 mb-2">Country</label>
                      <input 
                        v-model="userData.country" 
                        type="text" 
                        placeholder="Enter country" 
                        class="w-full px-4 py-3 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500 transition-all"
                      />
                    </div>
                    <div>
                      <label class="block text-sm font-medium text-gray-700 mb-2">Postal Code</label>
                      <input 
                        v-model="userData.pincode" 
                        type="number" 
                        placeholder="Enter postal code" 
                        class="w-full px-4 py-3 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500 transition-all"
                      />
                    </div>
                  </div>
                </div>
              </div>

              <div class="flex justify-end">
                <button 
                  type="submit" 
                  :disabled="saving"
                  class="px-8 py-3 text-white font-semibold rounded-lg shadow-md hover:shadow-lg transform hover:scale-105 transition-all duration-200 disabled:opacity-60 disabled:transform-none"
                  style="background-color: #207BFA;"
                >
                  <span v-if="saving" class="flex items-center gap-2">
                    <svg class="animate-spin h-5 w-5" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24">
                      <circle class="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" stroke-width="4"></circle>
                      <path class="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"></path>
                    </svg>
                    Saving...
                  </span>
                  <span v-else>Save Changes</span>
                </button>
              </div>
            </form>
        </div>
      </div>
    </div>

    <LogoutModal 
      :show="showLogoutConfirm" 
      @confirm="confirmLogout" 
      @cancel="cancelLogout" 
    />
  </div>
</template>